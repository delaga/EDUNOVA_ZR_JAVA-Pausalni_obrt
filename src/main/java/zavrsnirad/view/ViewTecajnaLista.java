    public ViewTecajnaLista() {
        initComponents();
        String json = Utils.ucitajURL("http://api.hnb.hr/tecajn/v2");
        if (json == null) {
            return;
        }
        java.lang.reflect.Type tecajType = new com.google.gson.reflect.TypeToken<ArrayList<Tecaj>>() {
        }.getType();
        List<Tecaj> lista = new Gson().fromJson(json, tecajType);
        txtBrojTecajnice.setText(lista.get(0).getBroj_tecajnice());
        txtDatumTecaja.setText(lista.get(0).getDatum_primjene());

        DefaultTableModel m = (DefaultTableModel) tblTecajna.getModel();

        lista.forEach((t) -> {
            String niz[] = {
                t.getDrzava(),
                t.getDrzava_iso(),
                t.getValuta(),
                String.valueOf(t.getJedinica()),
                t.getKupovni_tecaj(),
                t.getSrednji_tecaj(),
                t.getProdajni_tecaj()
            };

            m.addRow(niz);

        });
    }

