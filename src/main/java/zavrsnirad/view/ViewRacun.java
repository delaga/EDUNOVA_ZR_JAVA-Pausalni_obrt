    private void btnSpremiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSpremiActionPerformed
        //provjera da li racun postoji
        
        //upozori ako je isti broj
        //ako nije spremi ga sa novim brojem
        
        this.r.setBroj_racuna(txtBrojRacuna.getText());
        this.r.setDatum_dospijeca(new java.sql.Date(dtcDatumDospijeca.getDate().getTime()));
        this.r.setDatum_isporuke( new java.sql.Date(dtcDatumIsporuke.getDate().getTime()));
        this.r.setDatum_izdavanja(new java.sql.Date(dtcDatumIzdavanja.getDate().getTime()));
        this.r.setVrijemeIzdavanja(Time.valueOf(timeVrijemeIzdavanja.getTime()));
        this.r.setKlijent_kupac((Klijent_kupac) cmbKlijentKupac.getSelectedItem());
        this.r.setNacin_placanja(txtNacinPlacanja.getText());
        this.r.setNapomena(txtfNapomena.getText());
        this.r.setVrstaRacuna(1);
        List<Stavka> stavke=new ArrayList<>();
        int brojRedova=tblStavke.getModel().getRowCount();
        for(int i=0; i<brojRedova;i++){
            stavke.add((Stavka) tblStavke.getModel().getValueAt(i, 0)) ;
        }
        
        this.r.setVrijemeKreiranja(Date.valueOf(LocalDate.now()));
        this.r.setVrijemePromjene(Date.valueOf(LocalDate.now()));
        this.r.setStavke(stavke);
        
        try {
            obradaRacun.spremi(r);
        } catch (DelagaException ex) {
            ex.printStackTrace();
        }
        //na kraju
        glavni.ucitaj();
        dispose();
    }//GEN-LAST:event_btnSpremiActionPerformed

    private void tblStavkeMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblStavkeMouseClicked

        Stavka s = (Stavka) tblStavke.getValueAt(tblStavke.getSelectedRow(), 0);
        //System.out.println(tblStavke.getValueAt(tblStavke.getSelectedRow(), 0));
        //comStavkaNaziv.setSelectedItem(s.getProizvod().getNaziv());
        //comStavkaNaziv.setSelectedIndex(s.getId());

        //ovo isto tako na kupca
        DefaultComboBoxModel<Usluga_proizvod> mp
                = (DefaultComboBoxModel<Usluga_proizvod>) cmbStavkaNaziv.getModel();

        for (int i = 0; i < mp.getSize(); i++) {
            if (mp.getElementAt(i).getId().equals(
                    s.getProizvod().getId())) {
                cmbStavkaNaziv.setSelectedIndex(i);
                break;
            }

        }

        txtStavkaCijena.setText(s.getProizvod().getCijena().toString());
        txtStavkaJedinicaMjere.setText(s.getProizvod().getJedinica_mjere());
        spnStavkaKolicina.setValue(s.getKolicina());
        txtStavkaOpis.setText(s.getProizvod().getOpis());
        txtStavkaIznos.setText("" + s.getIznosStavke());
    }//GEN-LAST:event_tblStavkeMouseClicked

